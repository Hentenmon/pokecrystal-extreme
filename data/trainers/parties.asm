; Suomeksi
; Näin lukea tätä tiedostoa:
; 1. Lukee db "kouluttajan nimi@"
; 2. Lukee Trainertyyppi. Lue se.
; 3. Jos se on TRAINERTYPE_DVS, jatka vaiheeseen 5.
; 4. Jos se on TRAINERTYPE_ITEM_MOVES, jatka vaiheeseen 6.
; 5. Aluksi lukee "db", sitten level, pokemon, dv:t(hidden power tyyppiä varten.) Jos DV:t ovat $11, $11, niin DV:t ovat 15 kaikissa. Tämän jälkeen lukee held item ja 4 liikettä.
; 6. Aluksi lukee "db", sitten level, pokemon, held item ja 4 liikettä.
; Jos on NO_MOVE, se meinaa että se on tyhjä liike eikä sitä käytetä.
; Hidden Power koodit
;   $CC = Hidden Power Fighting
;   $CD = Hidden Power Flying
;   $CE = Hidden Power Poison, ei käytetä
;   $CF = Hidden Power Ground
;   $DC = Hidden Power Rock
;   $DD = Hidden Power Bug
;   $DE = Hidden Power Ghost
;   $DF = Hidden Power Steel
;   $EC = Hidden Power Fire
;   $ED = Hidden Power Water
;   $EE = Hidden Power Grass
;   $EF = Hidden Power Electric
;   $FC = Hidden Power Psychic, ei käytetä
;   $FD = Hidden Power Ice
;   $FE = Hidden Power Dragon
;   $FF = Listan loppu, ei käytetä

; In English
; Here's how to read the file:
; 1. Says db "trainer name@"
; 2. Says the trainertype. Read it.
; 3. If it is TRAINERTYPE_DVS, continue to step 5.
; 4. If it is TRAINERTYPE_ITEM_MOVES, continue to step 6.
; 5. It starts with "db", then the level, pokemon, dv:s(for the hidden power type.) If the DV:s are $11, $11, then DV:s in all stats are 15. After this is the held item and 4 moves.
; 6. It starts with "db", then the level, pokemon, held item and 4 moves.
; If one of the moves says NO_MOVE, it means that it's an empty moveslot and isn't used. 
; Hidden Power codes
;   $CC = Hidden Power Fighting
;   $CD = Hidden Power Flying
;   $CE = Hidden Power Poison, isn't used
;   $CF = Hidden Power Ground
;   $DC = Hidden Power Rock
;   $DD = Hidden Power Bug
;   $DE = Hidden Power Ghost
;   $DF = Hidden Power Steel
;   $EC = Hidden Power Fire
;   $ED = Hidden Power Water
;   $EE = Hidden Power Grass
;   $EF = Hidden Power Electric
;   $FC = Hidden Power Psychic, isn't used
;   $FD = Hidden Power Ice
;   $FE = Hidden Power Dragon
;   $FF = End of the list, isn't used

